// ^https?:\/\/
// - 문자열이 반드시 'http://' 또는 'https://'로 시작해야 함을 의미합니다.

// [\w.-]+
// - 그 뒤에는 도메인 이름이 위치하며, 알파벳/숫자/밑줄/하이픈/점 등을 포함할 수 있습니다.
//   예: 'www', 'example', 'my-site' 등

// (\.[\w\.-]+)+
// - 하나 이상의 점(.)으로 시작하는 최상위 도메인(TLD)이 있어야 하며, 예: '.com', '.co.kr', '.org' 등

// ([\/\w\.-]*)*\/?
// - 이후 경로가 올 수 있으며, 슬래시(/)와 문자/숫자/하이픈/점 등을 포함할 수 있습니다.
//   예: '/page', '/user/info' 등
// - 마지막 슬래시는 있어도 되고 없어도 됩니다.

// (\?.*)?
// - 선택적으로 물음표(?)로 시작하는 쿼리 파라미터가 있을 수 있습니다.
//   예: '?id=123', '?search=abc'

// $
// - 이 패턴은 문자열 끝까지 정확히 일치해야 함을 의미합니다.
export const isValidURL = (url: string): boolean => {
  const pattern = /^(https?:\/\/)[\w.-]+(\.[\w\.-]+)+([\/\w\.-]*)*\/?(\?.*)?$/;
  return pattern.test(url.trim());
};
